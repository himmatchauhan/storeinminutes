         * The function is updating the label `Select by date` so the user will see
         * what date range he is using at the moment to see the orders
         *
         * @param {object} start - Start date
         * @param {object} end - End date
         */
        function cb( start, end, label ) {
            // default text will be `All`
            var text = "All";
            /* when the user is selecting a date range that is not `All` the moment will return on `isValid()` true
            so this way we know it is not `All` */
            if ( moment(start).isValid() && moment(end).isValid() ) {
                text = (start.format('MMMM D, YYYY') === end.format('MMMM D, YYYY')) ? start.format('MMMM D, YYYY') : (start.format('MMMM D, YYYY') + ' - ' + end.format('MMMM D, YYYY'));                
            }
            if ( label.length > 0 && label != rangesList[7].name ) {
            	text = label;
            }
            // update the label
            $('#reportrange span').html(text);
        }
        // translate the date-picker ranges 
        var dp_ranges = {};
        dp_ranges[translations.datepicker.all] = ['',''];
        dp_ranges[translations.datepicker.today] = [moment(),moment()];
        dp_ranges[translations.datepicker.yesterday] = [moment().subtract(1,'days'),moment().subtract(1,'days')];
        dp_ranges[translations.datepicker.lastXDays.replace('{{days_number}}','7')] = [moment().subtract(6,'days'),moment()];
        dp_ranges[translations.datepicker.lastXDays.replace('{{days_number}}','30')] = [moment().subtract(29,'days'),moment()];
        dp_ranges[translations.datepicker.thisMonth] = [moment().startOf('month'),moment().endOf('month')];
        dp_ranges[translations.datepicker.lastMonth] = [moment().subtract(1,'month').startOf('month'),moment().subtract(1,'month').endOf('month')];
        /**
         * Date range picker initialize.
         *
         * Documentation: http://www.daterangepicker.com/#usage
         */
        $('#reportrange').daterangepicker({
            opens: "right",
            startDate: start,
            endDate: end,
            ranges: dp_ranges,
            locale: GetDateRangePickerLocalization()
        }, cb).on('apply.daterangepicker', function(ev, picker) {
        	// get objects
        	var startDate = picker.startDate.format('YYYY-MM-DD');
        	var endDate = picker.endDate.format('YYYY-MM-DD');
        	var selctedRange = 4;
            // user selected `all` - start date is invalid so we reset it
            if ( !picker.startDate.isValid() ) {
            	startDate = '';
            }
            // user selected `all` - end date is invalid so we reset it
            if ( !picker.endDate.isValid() ) {
            	endDate = '';
            }
            $.each(rangesList,function( key, value ) {
            	// skip on not relevant range
				if ( value.name != picker.chosenLabel ) return;
				selctedRange = key;
            });
            // save cookie with custom range dates
            if ( selctedRange == '7' ) {
            	var cookeValue = '7|'+ startDate + '|' + endDate;
            // save cookie with range type
            } else {
            	var cookeValue = selctedRange;
            }
            // update date range picker label
            if ( selctedRange != '7' ) {
            	$('#reportrange span').html(rangesList[selctedRange].name); 
            } 
            // initialize cookie expiry date
			var expiryDate = new Date().getTime()+(2*24*60*60*1000);
			// set cookie for two days
			document.cookie = websiteID+'-s123-ordersStatistics' + '='+cookeValue+'; expires=' + new Date(expiryDate) + '; path=/';
            // reload the page with the new parameters
            GetOrdersStatistics(websiteID,startDate,endDate,true);
        });
        // updating label
        cb(start,end,label);
        // set dates range
        start = start.length == 0 ? '' : start.format('YYYY-MM-DD');
        end = end.length == 0 ? '' : end.format('YYYY-MM-DD');
		// Get the orders statistics
		GetOrdersStatistics(websiteID,start,end,false);
	}

