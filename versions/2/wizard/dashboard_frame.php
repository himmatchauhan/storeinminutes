		/**
		 * The function display upcoming bookings
		 * 
		 * @param {array} events - The events to display
		 * @returns {void}	
		 */
		function displayUpcomingBookings( events ) {
			// get the container
			const $container = $('#bookingsWidgetBOX .upcoming-bookings-list');
			// clear the container
			$container.empty();
			// check if there are no events to display	
			if ( events.length === 0 ) {
				$container.html(`
					<div class="text-center">
						<p>${"Sorry, you have no upcoming bookings yet."}</p>
					</div>
				`);
				return;
			}
			// sort events by start time
			events.sort((a, b) => moment(a.start).diff(moment(b.start)));
			// group events by date
			const groupedEvents = {};
			events.forEach(event => {
				// get the start time
				const startTime = moment(event.start);
				// get the date key	
				const dateKey = startTime.format('YYYY-MM-DD');
				// check if the date key is not in the grouped events
				if ( !groupedEvents[dateKey] ) {
					groupedEvents[dateKey] = [];
				}
				// add the event to the date key
				groupedEvents[dateKey].push(event);
			});

			/**
			 * Creating list of upcoming bookings by ascending date
			 */
			Object.keys(groupedEvents).forEach(dateKey => {
				// get the date events
				const dateEvents = groupedEvents[dateKey];
				// get the date header
				const dateHeader = moment(dateKey).format('DD, YYYY');
				const monthName = monthOfYear[moment(dateKey).month()];
				// create the date section
				const $dateSection = $('<div class="date-section"></div>');
				// add the date header
				$dateSection.append(`<h4 class="booking-date" style="font-weight: bold;">${monthName} ${dateHeader}</h4>`);
				/**
				 * Loop through the events
				 */	
				dateEvents.forEach(event => {
					// get the start time
					const startTime = moment(event.start);
					// get the end time
					// check if end time exists and has hours, otherwise set to 24:00
					const endTime = event.end ? moment(event.end) : null;
					const endTimeFormatted = event.isFullDay ? '24:00' : endTime.format('HH:mm');
					// create event item
					const $eventItem = $(`
						<div class="list-group-item" data-event-id="${event.id}" data-event-unique-code-id="${event.uniqueCodeID}" data-event-calendar-id="${event.calendarID}" data-date-key="${startTime.format('YYYY-MM-DD HH:mm:ss')}" data-module-type-num="${moduleTypeNUM}" ${event.isExternalCalendar ? `data-is-external-calendar="${event.isExternalCalendar}" data-calendar-id="${event.calendarID}" data-access-token-unique-id="${event.accessTokenUniqueID}" data-calendar-provider="${event.calendarProvider}"` : ``} data-is-blocked-time="${event.isBlockedTime}" style="cursor: pointer;">
							<div class="flex flex-div-center-vertical">
								<span>${startTime.format('HH:mm')} - ${endTimeFormatted}</span>
								<span style="margin: 0 5px;">•</span>
								<span>${event.title}</span>
							</div>
						</div>
					`);
					// add click handler to show service info modal
					$eventItem.on('click', function() {
						// get the event item
						let $this = $(this);
						let src = '';
						// external calendar
						if ( $this.data('is-external-calendar') ) {
							src = `/versions/2/wizard/modules/scheduleBookingV2/calendarAPI/internalCalendar/bookingServices/externalEventIfno.php?eventID=${$this.data('event-unique-code-id')}&calendarID=${$this.data('calendar-id')}&w=${websiteID}&moduleTypeNUM=${$this.data('module-type-num')}&accessTokenUniqueID=${$this.data('access-token-unique-id')}&calendarProvider=${$this.data('calendar-provider')}`;
						// internal calendar
						} else if ( $this.data('is-blocked-time') ) {
							src = `/versions/2/wizard/modules/scheduleBookingV2/calendarAPI/internalCalendar/bookingServices/blockedTimeEventInfo.php?eventID=${$this.data('event-unique-code-id')}&calendarID=${$this.data('calendar-id')}&w=${websiteID}&moduleTypeNUM=${$this.data('module-type-num')}`;
						} else {
							src = `/versions/2/wizard/modules/scheduleBookingV2/calendarAPI/internalCalendar/bookingServices/serviceEventInfo.php?w=${websiteID}&eventID=${$this.data('event-unique-code-id')}&moduleID=${$this.data('module-type-num')}&scheduleSelectedDate=${$this.data('date-key')}`;
						}
						// destroy the modal
						topWindow.SystemModals.sm_destroy('showBookingInfoModal');
						// Initialize modal
						topWindow.SystemModals.sm_render({
							id: 'showBookingInfoModal',
							disposable: true,
							$parent: topWindow.$('body'), 
							mode: 'offcanvas',
							sizeClasses: 'm1',
							offCanvasDirection: 'reverse',
							headerSettings: {
								id: 'showBookingInfoModalTitle',
								title: "Service Info",
							},
							bodySettings: {
								content: `<iframe src="${src}" style="width:100%; height:500px; border:none;"></iframe>`
							},
							footerSettings: {
								isActive: false,
								buttons: []
							}
						});
						// show the modal
						topWindow.SystemModals.sm_get('showBookingInfoModal').show();
					});
					// add event item to the date section
					$dateSection.append($eventItem);
				});
				// add date section to the container
				$container.append($dateSection);
			});
