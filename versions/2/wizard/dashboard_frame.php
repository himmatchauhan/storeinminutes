	function InitializeOrdersStatistics( websiteID ) {
		// Get properties
        var label = translations.datepicker.lastXDays.replace('{{days_number}}','30');
        var start = moment().subtract(29,'days');
        var end = moment();
		var rangesList = {
			0: {
				name: translations.datepicker.all,
				ranges: ['',''],
			},
			1: {
				name: translations.datepicker.today,
				ranges: [moment(),moment()],
			},
			2: {
				name: translations.datepicker.yesterday,
				ranges: [moment().subtract(1,'days'),moment().subtract(1,'days')],
			},
			3: {
				name: translations.datepicker.lastXDays.replace('{{days_number}}','7'),
				ranges: [moment().subtract(6,'days'),moment()],
			},
			4: {
				name: translations.datepicker.lastXDays.replace('{{days_number}}','30'),
				ranges: [moment().subtract(29,'days'),moment()],
			},
			5: {
				name: translations.datepicker.thisMonth,
				ranges: [moment().startOf('month'),moment().endOf('month')],
			},
			6: {
				name: translations.datepicker.lastMonth,
				ranges: [moment().subtract(1,'month').startOf('month'),moment().subtract(1,'month').endOf('month')],
			},
			7: {
				name: translations.datepicker.customRange,
				ranges: ['',''],
			},
		};
		// get cookies
		var cookies = document.cookie ? document.cookie.split('; ') : [];
		// search for relevant cookie
		for ( var i = 0; i < cookies.length; i++ ) {
			var parts = cookies[i].split('=');
			// skip on not relevant cookies
			if ( decodeURIComponent(parts[0]) !== websiteID+'-s123-ordersStatistics' ) continue;
			// get cookie
			var cookie = parts.slice(1).join('=');
			cookie = decodeURIComponent(cookie);
			cookie = cookie.split('|');
			// set start date and end date according to the last selected ranges
			if ( cookie.length == 1 ) {
				start = rangesList[cookie[0]].ranges[0];
				end = rangesList[cookie[0]].ranges[1];
				label = rangesList[cookie[0]].name;
			// set start date and end date of custom range
			} else if ( cookie.length == 3 && cookie[0] == '7' ) {
				start = moment(cookie[1]);
				end = moment(cookie[2]);
				label = '';
			}
		}
