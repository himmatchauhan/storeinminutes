				SystemModals.sm_render({
					id: 'messageInfoModal',
					disposable: false,
					$parent: $('body'),
					sizeClasses: 'size-s2',
					headerSettings: {
						title: '<div>'+"Message"+' '+message_id+'</div>',
					},
					bodySettings: { // body settings
						id: 'messageInfoBodyModal',
						content: html,
					},
					footerSettings: { // footer settings
						isActive: true,
						buttons: [
							{
								class: 'btn print-msg-btn hide-from-print',
								text: "Print"							},
							{
								class: 'btn btn-primary hide-from-print',
								dataAttr: 'data-dismiss="modal"',
								text: "OK",
							}
						]
					},
					showCallback: function ( event, modal ) {
						// print btn event
						modal.$html.find('.print-msg-btn').off('click').on('click',function() {
							// print message
							printElement('.print-content');
						});
