function redirectOldBrowserToUpgrade() {
    //https://github.com/bestiejs/platform.js/

    /*
    console.log(platform.name);
    console.log(platform.version);
    console.log(platform.product);
    console.log(platform.os);
    */

    // We make sure the platform object is defined before we use it
    if ( !platform ) return;

    // Make sure we don't use it in the upgrade browser page so we don't have unlimited refresh
    if (platform.name === null || window.location.href.indexOf('login/upgrade_browser.php') !== -1) {
        return;
    }

    // Add extra data to the tracking errors
    if (window.TrackJS) {
        trackJs.addMetadata("platformName",platform.name);
        trackJs.addMetadata("platformVersion",platform.version);
        trackJs.addMetadata("platformOS",platform.os);
    }

    var p_version = (platform.version !== null) ? platform.version.toString().split(".")[0] : '0';

    if (platform.name=='Safari' && (p_version=='5' || p_version=='6')) {
        window.location.replace("/manager/login/upgrade_browser.php");
    }
}
